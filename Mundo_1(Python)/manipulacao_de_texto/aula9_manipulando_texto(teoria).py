## Fatiamento

# frase[9]>> Aparece a letra que esta no posicionamento 9
# frase[1:6]>> Aparece as letras que estao no posicionamento 1 até o 5
# frase[1:10:2]>> Aparece as letras que estao no posicionamento 1 até o 10, 
# pulando de "um em um"
# frase[:5]>> Aparece as letras do posicionamento 0 até o 4
# frase[7:]>> Aparece as letras do posicionamento 7 até o final da frase

## Analise

# LEN >> Mostra a quantidade de caracteres tem a frase
# COUNT('x')>> Conta quantos x tem na frase
#                ou
# COUNT('x',0,13)>> Contas quantos X entre o posicionamento 0 e 13
# FIND(deo)>> Exemplo : video > 3,4,5 >> Mostra aonde começa tal conjunto de letras
# IN >> Exemplo : 'curso' IN frase > Existe a palvra 'curso' dentro da frase ?

## Transformação

# REPLACE >> reposiciona
# UPPER >> minuscula p/ maiuscula
# LOWER >> maiuscula p/ minuscula
# CAPITALIZE >> Todas as letras em minuscula, só o primeiro caractere em maiuscula
# TITLE >> Começo de cada palavra em maiuscula
# STRIP >> Remove todos os espaços desnecessário do começo e do final da frase
# RSTRIP >> Remove só os ultimos espaços desnecessários (direita)
# LSTRIP >> Remove só os primeiros espaços desnecessários (esquerda)

## Divisão

# SPLIT >> Separa cada palavra da frase

